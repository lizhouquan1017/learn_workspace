# coding:utf-8

import sys

# 显示本地字节序列指示符
print(sys.byteorder)
# 显示与Python解释器有关的版权信息
print(sys.copyright)
# 显示Python解释器在磁盘上的存储路径
print(sys.executable)
# 显示当前系统中保存文件的字符集
print(sys.getfilesystemencoding())
# 整数支持的最大值
print(sys.maxsize)
# 解释器所在平台
print(sys.platform)
# Python的版本信息
print(sys.version)
# 解释器的主版本号
print(sys.winver)
